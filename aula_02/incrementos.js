let numero = 1;


// Incrementação
console.log("O valor atual da variavel é: ", numero);
console.log("O valor pos-incremento da variavel é: ", ++numero);

numero = 10

console.log("\nO valor atual da variavel é: ", numero);
console.log("O valor da variavel pre-incremento é: ", --numero)

// console.log("O valor pos incrementado da variável atualizada: ", numero)

// Atribuição
console.log("\nAtribucao com soma (numero = numero + 2): ", numero += 2);

